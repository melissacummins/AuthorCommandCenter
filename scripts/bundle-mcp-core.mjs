// Pre-bundles the MCP connector's data core into a single, self-contained
// ES module inside api/, so the Vercel serverless function never reaches
// across into ../src at runtime.
//
// WHY THIS EXISTS
// The project is "type": "module", so the function Vercel compiles from
// api/mcp.ts runs under Node's native ESM loader. That loader refuses to
// resolve extension-less relative imports — and every import in src/ is
// extension-less (Vite/tsc resolve those at build time, Node does not).
// A raw `import … from '../src/lib/dashboardCore'` therefore threw
// ERR_MODULE_NOT_FOUND at cold start and the whole /mcp surface 500'd.
//
// esbuild inlines dashboardCore + its (pure, type-only-dependent) helpers
// into one file with NO relative imports. npm packages stay external —
// bare specifiers like '@supabase/supabase-js' resolve fine from
// node_modules; only relative extension-less paths were the problem.
//
// Runs automatically before `npm run build` (see package.json "prebuild"),
// and can be run on its own with `npm run bundle:mcp`. The output is
// committed so the function builds even if the lifecycle order ever shifts.

import { build } from 'esbuild';

await build({
  entryPoints: ['src/lib/dashboardCore.ts'],
  outfile: 'api/_generated/dashboardCore.js',
  bundle: true,
  platform: 'node',
  format: 'esm',
  target: 'node20',
  packages: 'external', // keep node_modules deps as runtime bare imports
  logLevel: 'info',
  banner: {
    js: '// GENERATED by scripts/bundle-mcp-core.mjs — do not edit by hand.\n'
      + '// Source of truth: src/lib/dashboardCore.ts. Run `npm run bundle:mcp` to refresh.',
  },
});
